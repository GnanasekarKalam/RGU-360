/**
 * Export report to PowerPoint (.pptx) format
 */
export async function exportToPPT(
  reportData: ReportData,
  options: ExportOptions
): Promise<Buffer> {
  try {
    const pres = new PptxGenJS();
    pres.defineLayout({ name: 'TITLE_SLIDE', master: 'BLANK' });

    // Slide 1: Title Slide
    let slide = pres.addSlide();
    slide.background = { color: '4472C4' };
    slide.addText(options.reportName, {
      x: 0.5,
      y: 2,
      w: 9,
      h: 2,
      fontSize: 44,
      bold: true,
      color: 'FFFFFF',
      align: 'center',
    });
    slide.addText(`Academic Year: ${options.academicYear || 'N/A'}`, {
      x: 0.5,
      y: 4.5,
      w: 9,
      h: 0.8,
      fontSize: 18,
      color: 'FFFFFF',
      align: 'center',
    });
    slide.addText(`Generated on: ${new Date().toLocaleDateString()}`, {
      x: 0.5,
      y: 5.5,
      w: 9,
      h: 0.6,
      fontSize: 14,
      color: 'D9E1F2',
      align: 'center',
    });

    // Slide 2: Summary
    if (reportData.summary && Object.keys(reportData.summary).length > 0) {
      slide = pres.addSlide();
      slide.addText('Summary', {
        x: 0.5,
        y: 0.5,
        w: 9,
        fontSize: 32,
        bold: true,
        color: '4472C4',
      });

      const summaryItems = Object.entries(reportData.summary).map(([key, value]) => [
        key,
        typeof value === 'object' ? JSON.stringify(value) : String(value),
      ]);

      slide.addTable(summaryItems, {
        x: 0.5,
        y: 1.5,
        w: 9,
        colW: [3, 6],
        border: { pt: 1, color: 'CCCCCC' },
        fill: { color: 'F2F2F2' },
        rowH: 0.5,
        fontSize: 12,
      });
    }

    // Slide 3: Key Observations (if data available)
    if (reportData.data && reportData.data.length > 0) {
      slide = pres.addSlide();
      slide.addText('Key Observations', {
        x: 0.5,
        y: 0.5,
        w: 9,
        fontSize: 32,
        bold: true,
        color: '4472C4',
      });

      const observations = [
        `Total Records: ${reportData.data.length}`,
        `Report Type: ${reportData.metadata.reportType || 'General'}`,
        `Generated By: ${options.generatedBy || 'System'}`,
        `Department: ${options.department || 'All Departments'}`,
      ];

      observations.forEach((obs, idx) => {
        slide.addText(obs, {
          x: 1,
          y: 1.5 + idx * 0.8,
          w: 8.5,
          fontSize: 14,
          bullet: true,
        });
      });
    }

    // Slide 4: Data Sample (first 10 records)
    if (reportData.data && reportData.data.length > 0) {
      slide = pres.addSlide();
      slide.addText('Data Sample', {
        x: 0.5,
        y: 0.5,
        w: 9,
        fontSize: 32,
        bold: true,
        color: '4472C4',
      });

      const columns = Object.keys(reportData.data[0]).slice(0, 4); // Limit to 4 columns
      const tableData = [columns];

      reportData.data.slice(0, 10).forEach(row => {
        tableData.push(columns.map(col => String(row[col] || '')));
      });

      slide.addTable(tableData, {
        x: 0.5,
        y: 1.5,
        w: 9,
        colW: [2.25, 2.25, 2.25, 2.25],
        border: { pt: 1, color: 'CCCCCC' },
        fill: { color: 'D9E1F2' },
        rowH: 0.4,
        fontSize: 10,
      });

      if (reportData.data.length > 10) {
        slide.addText(`... and ${reportData.data.length - 10} more records`, {
          x: 0.5,
          y: 6.5,
          w: 9,
          fontSize: 12,
          italics: true,
          color: 'FF0000',
        });
      }
    }

    // Slide 5: Conclusion
    slide = pres.addSlide();
    slide.addText('Conclusion', {
      x: 0.5,
      y: 0.5,
      w: 9,
      fontSize: 32,
      bold: true,
      color: '4472C4',
    });
    slide.addText(
      'This report has been generated by the Mathematics Dashboard System. For detailed analysis and filtering, please refer to the Excel export.',
      {
        x: 1,
        y: 2,
        w: 8,
        fontSize: 14,
        align: 'center',
      }
    );

    const buffer = await pres.write({ outputType: 'arraybuffer' });
    return Buffer.from(buffer);
  } catch (error) {
    console.error('Error exporting to PPT:', error);
    throw new Error(`PowerPoint export failed: ${error instanceof Error ? error.message : 'Unknown error'}`);
  }
}

