/**
 * Export report to PDF format
 */
export async function exportToPDF(
  reportData: ReportData,
  options: ExportOptions
): Promise<Buffer> {
  return new Promise((resolve, reject) => {
    try {
      const doc = new PDFDocument({
        size: 'A4',
        margin: 50,
      });

      const chunks: Buffer[] = [];

      doc.on('data', (chunk: Buffer) => chunks.push(chunk));
      doc.on('end', () => resolve(Buffer.concat(chunks)));
      doc.on('error', reject);

      // Title
      doc.fontSize(24).font('Helvetica-Bold').text(options.reportName, { align: 'center' });
      doc.moveDown(0.5);

      // Metadata
      doc.fontSize(10).font('Helvetica').fillColor('#666666');
      doc.text(`Generated on: ${new Date().toLocaleString()}`, { align: 'center' });
      doc.text(`Academic Year: ${options.academicYear || 'N/A'}`, { align: 'center' });
      doc.text(`Department: ${options.department || 'All Departments'}`, { align: 'center' });
      doc.moveDown(1);

      // Divider line
      doc.strokeColor('#CCCCCC').lineWidth(1).moveTo(50, doc.y).lineTo(545, doc.y).stroke();
      doc.moveDown(0.5);

      // Summary Section
      if (reportData.summary) {
        doc.fontSize(14).font('Helvetica-Bold').fillColor('#000000').text('Summary', { underline: true });
        doc.moveDown(0.3);

        Object.entries(reportData.summary).forEach(([key, value]) => {
          doc.fontSize(10).font('Helvetica');
          if (typeof value === 'object') {
            doc.text(`${key}: ${JSON.stringify(value)}`);
          } else {
            doc.text(`${key}: ${value}`);
          }
        });

        doc.moveDown(0.5);
      }

      // Data Table
      if (reportData.data && reportData.data.length > 0) {
        doc.fontSize(14).font('Helvetica-Bold').fillColor('#000000').text('Data', { underline: true });
        doc.moveDown(0.3);

        // Table headers
        const columns = Object.keys(reportData.data[0]);
        const columnWidth = 500 / columns.length;

        doc.fontSize(9).font('Helvetica-Bold').fillColor('#FFFFFF');
        doc.rect(50, doc.y, 500, 20).fill('#4472C4');
        columns.forEach((col, idx) => {
          doc.text(
            col.substring(0, 15),
            50 + idx * columnWidth,
            doc.y - 17,
            { width: columnWidth - 5, align: 'left' }
          );
        });

        doc.moveDown(1.2);

        // Table rows (limit to first 50 rows for PDF size)
        doc.fontSize(8).font('Helvetica').fillColor('#000000');
        const rowsToShow = Math.min(reportData.data.length, 50);

        reportData.data.slice(0, rowsToShow).forEach((row, idx) => {
          if (doc.y > 750) {
            doc.addPage();
          }

          const rowColor = idx % 2 === 0 ? '#F2F2F2' : '#FFFFFF';
          doc.rect(50, doc.y, 500, 15).fill(rowColor);
          doc.fillColor('#000000');

          columns.forEach((col, colIdx) => {
            const value = String(row[col] || '').substring(0, 20);
            doc.text(
              value,
              50 + colIdx * columnWidth,
              doc.y - 12,
              { width: columnWidth - 5, align: 'left' }
            );
          });

          doc.moveDown(1.2);
        });

        if (reportData.data.length > rowsToShow) {
          doc.fontSize(9).fillColor('#FF0000')
            .text(`... and ${reportData.data.length - rowsToShow} more records (see Excel export for full data)`);
        }
      }

      // Footer
      doc.moveDown(1);
      doc.fontSize(8).fillColor('#999999').text(
        `Page ${doc.bufferedPageRange().count}`,
        { align: 'center' }
      );

      doc.end();
    } catch (error) {
      reject(new Error(`PDF export failed: ${error instanceof Error ? error.message : 'Unknown error'}`));
    }
  });
}

