/**
 * Export report to CSV format
 */
export async function exportToCSV(
  reportData: ReportData,
  options: ExportOptions
): Promise<string> {
  try {
    if (!reportData.data || reportData.data.length === 0) {
      return 'No data to export';
    }

    const parser = new Parser({
      fields: Object.keys(reportData.data[0]),
      header: true,
    });

    const csv = parser.parse(reportData.data);
    
    // Add header comments
    const header = [
      `Report Name: ${options.reportName}`,
      `Generated Date: ${new Date().toLocaleDateString()}`,
      `Generated By: ${options.generatedBy || 'System'}`,
      `Academic Year: ${options.academicYear || 'N/A'}`,
      '',
    ].join('\n');

    return header + csv;
  } catch (error) {
    console.error('Error exporting to CSV:', error);
    throw new Error(`CSV export failed: ${error instanceof Error ? error.message : 'Unknown error'}`);
  }
}

