/**
 * Export report to Word (.docx) format
 */
export async function exportToWord(
  reportData: ReportData,
  options: ExportOptions
): Promise<Buffer> {
  try {
    const sections = [];

    // Title and metadata section
    sections.push(
      new docx.Paragraph({
        text: options.reportName,
        heading: docx.HeadingLevel.HEADING_1,
        alignment: docx.AlignmentType.CENTER,
        spacing: { after: 200 },
        style: 'Title',
      }),
      new docx.Paragraph({
        text: `Generated on: ${new Date().toLocaleString()}`,
        alignment: docx.AlignmentType.CENTER,
        spacing: { after: 100 },
      }),
      new docx.Paragraph({
        text: `Academic Year: ${options.academicYear || 'N/A'}`,
        alignment: docx.AlignmentType.CENTER,
        spacing: { after: 100 },
      }),
      new docx.Paragraph({
        text: `Department: ${options.department || 'All Departments'}`,
        alignment: docx.AlignmentType.CENTER,
        spacing: { after: 400 },
      }),
      new docx.Paragraph({
        text: '',
        border: {
          bottom: {
            color: 'CCCCCC',
            space: 1,
            style: docx.BorderStyle.SINGLE,
            size: 6,
          },
        },
      })
    );

    // Summary section
    if (reportData.summary && Object.keys(reportData.summary).length > 0) {
      sections.push(
        new docx.Paragraph({
          text: 'Summary',
          heading: docx.HeadingLevel.HEADING_2,
          spacing: { before: 200, after: 200 },
        })
      );

      Object.entries(reportData.summary).forEach(([key, value]) => {
        sections.push(
          new docx.Paragraph({
            text: `${key}: ${typeof value === 'object' ? JSON.stringify(value, null, 2) : value}`,
            spacing: { after: 100 },
          })
        );
      });

      sections.push(new docx.Paragraph({ text: '', spacing: { after: 200 } }));
    }

    // Data table section
    if (reportData.data && reportData.data.length > 0) {
      sections.push(
        new docx.Paragraph({
          text: 'Detailed Report Data',
          heading: docx.HeadingLevel.HEADING_2,
          spacing: { before: 200, after: 200 },
        })
      );

      const columns = Object.keys(reportData.data[0]);
      const tableRows = [
        new docx.TableRow({
          cells: columns.map(
            col =>
              new docx.TableCell({
                children: [
                  new docx.Paragraph({
                    text: col,
                    bold: true,
                    color: 'FFFFFF',
                  }),
                ],
                shading: { fill: '4472C4', type: 'clear' },
              })
          ),
        }),
      ];

      // Add data rows (limit to 100 rows to avoid huge documents)
      reportData.data.slice(0, 100).forEach((row, idx) => {
        tableRows.push(
          new docx.TableRow({
            cells: columns.map(
              col =>
                new docx.TableCell({
                  children: [
                    new docx.Paragraph({
                      text: String(row[col] || ''),
                    }),
                  ],
                  shading: {
                    fill: idx % 2 === 0 ? 'F2F2F2' : 'FFFFFF',
                    type: 'clear',
                  },
                })
            ),
          })
        );
      });

      sections.push(
        new docx.Table({
          rows: tableRows,
          width: { size: 100, type: docx.WidthType.PERCENTAGE },
        })
      );

      if (reportData.data.length > 100) {
        sections.push(
          new docx.Paragraph({
            text: `\n... and ${reportData.data.length - 100} more records (see Excel export for full data)`,
            italics: true,
            color: 'FF0000',
            spacing: { before: 200 },
          })
        );
      }
    }

    // Signature section
    sections.push(
      new docx.Paragraph({ text: '', spacing: { before: 400 } }),
      new docx.Paragraph({
        text: 'Generated by Mathematics Dashboard System',
        alignment: docx.AlignmentType.CENTER,
        color: '999999',
        spacing: { before: 200 },
      })
    );

    const doc = new docx.Document({
      sections: [
        {
          properties: {},
          children: sections,
        },
      ],
    });

    const buffer = await docx.Packer.toBuffer(doc);
    return buffer;
  } catch (error) {
    console.error('Error exporting to Word:', error);
    throw new Error(`Word export failed: ${error instanceof Error ? error.message : 'Unknown error'}`);
  }
}

