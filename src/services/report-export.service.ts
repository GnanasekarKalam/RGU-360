// ============================================================================

/**
 * Export report to Excel format with multiple sheets
 */
export async function exportToExcel(
  reportData: ReportData,
  options: ExportOptions
): Promise<Buffer> {
  try {
    const workbook = new ExcelJS.Workbook();

    // Sheet 1: Summary
    const summarySheet = workbook.addWorksheet('Summary');
    summarySheet.columns = [
      { header: 'Property', key: 'property', width: 25 },
      { header: 'Value', key: 'value', width: 40 },
    ];

    // Add metadata
    summarySheet.addRows([
      { property: 'Report Name', value: options.reportName },
      { property: 'Generated Date', value: new Date().toLocaleDateString() },
      { property: 'Generated By', value: options.generatedBy || 'System' },
      { property: 'Academic Year', value: options.academicYear || 'N/A' },
      { property: 'Department', value: options.department || 'All' },
      { property: '', value: '' },
    ]);

    // Add summary statistics
    if (reportData.summary) {
      Object.entries(reportData.summary).forEach(([key, value]) => {
        summarySheet.addRow({
          property: key,
          value: typeof value === 'object' ? JSON.stringify(value) : value,
        });
      });
    }

    // Format summary sheet
    summarySheet.getRow(1).font = { bold: true, size: 12 };
    summarySheet.getRow(1).fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF4472C4' } };
    summarySheet.getRow(1).font = { bold: true, color: { argb: 'FFFFFFFF' } };

    // Sheet 2: Data
    if (reportData.data && reportData.data.length > 0) {
      const dataSheet = workbook.addWorksheet('Data');
      const columns = Object.keys(reportData.data[0]);

      dataSheet.columns = columns.map(col => ({
        header: col.charAt(0).toUpperCase() + col.slice(1),
        key: col,
        width: 15,
      }));

      // Add data rows
      reportData.data.forEach(row => {
        dataSheet.addRow(row);
      });

      // Format header row
      dataSheet.getRow(1).font = { bold: true };
      dataSheet.getRow(1).fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FFD9E1F2' } };

      // Auto-fit columns
      dataSheet.columns.forEach(col => {
        col.width = Math.min(col.width || 15, 50);
      });

      // Add alternating row colors
      dataSheet.eachRow((row, rowNumber) => {
        if (rowNumber > 1 && rowNumber % 2 === 0) {
          row.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FFF2F2F2' } };
        }
      });
    }

    // Sheet 3: Charts data (if available)
    if (reportData.charts && Object.keys(reportData.charts).length > 0) {
      const chartsSheet = workbook.addWorksheet('Charts');
      chartsSheet.columns = [
        { header: 'Chart Name', key: 'chartName', width: 25 },
        { header: 'Data', key: 'data', width: 50 },
      ];

      Object.entries(reportData.charts).forEach(([name, data]) => {
        chartsSheet.addRow({
          chartName: name,
          data: JSON.stringify(data),
        });
      });
    }

    // Generate buffer
    const buffer = await workbook.xlsx.writeBuffer();
    return buffer as Buffer;
  } catch (error) {
    console.error('Error exporting to Excel:', error);
    throw new Error(`Excel export failed: ${error instanceof Error ? error.message : 'Unknown error'}`);
  }
}

